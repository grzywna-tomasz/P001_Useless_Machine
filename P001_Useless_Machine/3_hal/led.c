/** \brief Function starting LED task
 *  \returns None
 */
void Led_Start(void)
{
   OS_Activate_Task(OS_TASK_LED_TOGGLE);
}

/** \brief Function for toggling development LED
 *  \returns None
 */
void Led_Toggle(void)
{
	IO_SetPin(PORTB, TEST_LED_INDEX, ~IO_GetPin(PORTB, TEST_LED_INDEX));
}